void Cube::Render()
{
  glBindVertexArray(Cube::cubeVAO);
  glDrawArrays(GL_TRIANGLES, 0, 36);
}

void Cube::UpdateColor(vec3 color)
{
  this->color = color / vec3(255.f);
}

vec3 Cube::GetColor()
{
    vec3 norm = vec3(255.f);
    return this->color * norm;
}
